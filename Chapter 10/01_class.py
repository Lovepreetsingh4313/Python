class Employee:
    language = "Py"
    dob = "14-feb"

lovi = Employee()
lovi.name = "Lovi Saini"
print(lovi.name, lovi.language, lovi.dob)   

preet = Employee()
preet.name = "Preet Tractor"
print(preet.name, preet.language, preet.dob)  


# Here name is an instance(object) attribute and language and dob are class attributes as they directly belongs to the class